class Result {
  int id;
  String raceId;
  String driver;
  String team;
  int position;
  String time;
  int grid;
  int laps;
  int points;

  Result({
    required this.id,
    required this.raceId,
    required this.driver,
    required this.team,
    required this.position,
    required this.time,
    required this.grid,
    required this.laps,
    required this.points,
  });
}
